package basics;

public class Expressions {
    public static void main(String[] args) {
        int x = 10;
        int y = 20;

        // using the variables in an expression
        int result = x + y; // assigning the return of the expression to a variable
        System.out.println(result);
        System.out.println(x + y); // printing the output of expression to the console

    }
}
