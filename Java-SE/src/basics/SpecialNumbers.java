package basics;

public class SpecialNumbers {
    public static void main(String[] args) {
        // binary numbers (base 2)
        int binaryNumber = 0b11;
        System.out.println("the integer equivivalent of '0b11' is : " + binaryNumber);

        // octal numbers (base 8)
        int octalNumber = 011;
        System.out.println("the integer equivivalent of '011' is : " + octalNumber);

        // hexadecimal numbers (base 16)
        int hex = 0xAA;
        System.out.println("the integer equivivalent of '0xAA' is : " + hex);
    }
}
