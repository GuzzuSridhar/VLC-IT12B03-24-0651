package methods;

public class Calculator {
    // methods to perform math operations
    float add(float n1, float n2) {
        return n1 + n2;
    }

    float subtract(float n1, float n2) {
        return n1 - n2;
    }

    float multiply(float n1, float n2) {
        return n1 * n2;
    }

    float divide(float n1, float n2) {
        return n1 / n2;
    }

    static float getPi() {
        return 3.14f;
    }

}
