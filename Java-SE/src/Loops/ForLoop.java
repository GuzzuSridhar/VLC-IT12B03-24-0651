package Loops;

public class ForLoop {
    public static void main(String[] args) {
        for (int i = 1; i < 11; i++) {
            System.out.println(i);
        }

        // print numbers in reverse order
        for (int i = 10; i >= 0; i--) {
            System.out.println(i);
        }
    }
}
